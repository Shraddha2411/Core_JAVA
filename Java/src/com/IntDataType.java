package com;

public class IntDataType {
	public static void main(String []args) {
		byte maxbyte=127;
		byte minbyte=-128;
		short maxshort=32767;
		short minshort=-32768;
		int maxint=2147483647;
		int minint=-2147483648;
		long maxlong=9223372036854775807L;
		long minlong=-9223372036854775808L;
	


System.out.println("Max range of byte is:"+maxbyte);
System.out.println("Min range of byte is:"+minbyte);
System.out.println("Max range of short is:"+maxshort);
System.out.println("Min range of short is:"+minshort);
System.out.println("Max range of int is:"+maxint);
System.out.println("Min range of int is:"+minint);
System.out.println("Max range of long is:"+maxlong);
System.out.println("Min range of long is:"+minlong);

}
}